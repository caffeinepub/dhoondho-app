const LANG_STORAGE_KEY = "dhoondho_lang";
const changeCallbacks: Array<(lang: string) => void> = [];

let currentLang = "en";

function detectBrowserLanguage(): string {
  const browserLang = (navigator.language || "").toLowerCase().split("-")[0];
  if (SUPPORTED_LANGUAGES.includes(browserLang as LangCode)) {
    return browserLang;
  }
  return "en";
}

function initLanguage(): void {
  const stored = localStorage.getItem(LANG_STORAGE_KEY);
  if (stored && SUPPORTED_LANGUAGES.includes(stored as LangCode)) {
    currentLang = stored;
  } else {
    currentLang = detectBrowserLanguage();
  }
}

initLanguage();

export function t(key: string): string {
  return TRANSLATIONS[currentLang]?.[key] ?? TRANSLATIONS.en[key] ?? key;
}

export function setLanguage(code: string): void {
  const lang = SUPPORTED_LANGUAGES.includes(code as LangCode) ? code : "en";
  currentLang = lang;
  localStorage.setItem(LANG_STORAGE_KEY, lang);
  for (const cb of changeCallbacks) {
    cb(lang);
  }
}

export function getCurrentLanguage(): string {
  return currentLang;
}

export function onLanguageChange(callback: (lang: string) => void): () => void {
  changeCallbacks.push(callback);
  return () => {
    const idx = changeCallbacks.indexOf(callback);
    if (idx !== -1) changeCallbacks.splice(idx, 1);
  };
}

